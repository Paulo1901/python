# comversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipo imutáveis e primitivos:
# str, int, float, bool

print(type(int('1')), int('1'))
print(float('1') + 1)
print(bool(''))
print(str(11) + 'b')
