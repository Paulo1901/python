# Python = Linguagem de programação
# Tipo de tipagem = Dinâmica / forte
# Tipagem dinâmica é o Python ja sabe o tipo de informação que você esta passando
# str -> string -> texto
# String são textos que estão dentro de aspas

# aspas simples - ''
print('Paulo Carvalho')
print("Paulo 'Carvalho'")

# aspas duplas - ""
print("Paulo Carvalho")
print('Paulo "Carvalho"')

# escapes - \n
print('Paulo \"Carvalho')
print(123) # o python ja sab que é um número inteiro
